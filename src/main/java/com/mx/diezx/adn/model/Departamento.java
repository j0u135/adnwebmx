package com.mx.diezx.adn.model;

public class Departamento {
	
	private int id_Departamento;
	private String nb_Departamento;
	private String desc_Departamento;
	private int id_Area;
	
	public int getId_Departamento() {
		return id_Departamento;
	}
	public void setId_Departamento(int id_Departamento) {
		this.id_Departamento = id_Departamento;
	}
	public String getNb_Departamento() {
		return nb_Departamento;
	}
	public void setNb_Departamento(String nb_Departamento) {
		this.nb_Departamento = nb_Departamento;
	}
	public String getDesc_Departamento() {
		return desc_Departamento;
	}
	public void setDesc_Departamento(String desc_Departamento) {
		this.desc_Departamento = desc_Departamento;
	}
	public int getId_Area() {
		return id_Area;
	}
	public void setId_Area(int id_Area) {
		this.id_Area = id_Area;
	}

}
