package com.mx.diezx.adn.model;

public class Puesto {
	
	private int idPuesto;
	private String nb_Puesto;
	private String desc_puesto;
	
	public int getIdPuesto() {
		return idPuesto;
	}
	public void setIdPuesto(int idPuesto) {
		this.idPuesto = idPuesto;
	}
	public String getNb_Puesto() {
		return nb_Puesto;
	}
	public void setNb_Puesto(String nb_Puesto) {
		this.nb_Puesto = nb_Puesto;
	}
	public String getDesc_puesto() {
		return desc_puesto;
	}
	public void setDesc_puesto(String desc_puesto) {
		this.desc_puesto = desc_puesto;
	}
	
	

}
