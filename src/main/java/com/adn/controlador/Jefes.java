package com.adn.controlador;

public class Jefes {

	private String idJefe;
	private String idSubordinado;
	private String idCreadoPor;
	private String fechaCreacion;
	
	/**
	 * @return the idJefe
	 */
	public String getIdJefe() {
		return idJefe;
	}
	/**
	 * @param idJefe the idJefe to set
	 */
	public void setIdJefe(String idJefe) {
		this.idJefe = idJefe;
	}
	/**
	 * @return the idSubordinado
	 */
	public String getIdSubordinado() {
		return idSubordinado;
	}
	/**
	 * @param idSubordinado the idSubordinado to set
	 */
	public void setIdSubordinado(String idSubordinado) {
		this.idSubordinado = idSubordinado;
	}
	/**
	 * @return the idCreadoPor
	 */
	public String getIdCreadoPor() {
		return idCreadoPor;
	}
	/**
	 * @param idCreadoPor the idCreadoPor to set
	 */
	public void setIdCreadoPor(String idCreadoPor) {
		this.idCreadoPor = idCreadoPor;
	}
	/**
	 * @return the fechaCreacion
	 */
	public String getFechaCreacion() {
		return fechaCreacion;
	}
	/**
	 * @param fechaCreacion the fechaCreacion to set
	 */
	public void setFechaCreacion(String fechaCreacion) {
		this.fechaCreacion = fechaCreacion;
	}


}
